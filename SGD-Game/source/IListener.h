//	Purpose		:	To provide a base interface for some game objects
//					to inherit from in order to recieve events.
//
////////////////////////////////////////////////////////////////////////////
#pragma once

#include "CEvent.h"

class IListener
{
public:
	///////////////////////////////////////////////////////////////////
	//	Function:	"IListener(Constructor)"
	///////////////////////////////////////////////////////////////////
	IListener(void) {}

	///////////////////////////////////////////////////////////////////
	//	Function:	"~IListener(Destructor)"
	///////////////////////////////////////////////////////////////////
	virtual ~IListener(void) {}

	////////////////////////////////////////////////////////////////////
	// Function: “HandleEvent”
	//
	// Purpose: Handles all events associated with this object.
	//			Blue print function - MUST be defined in the derived class.
	//			Mini-proc function for whenever this object receives and event.		
	////////////////////////////////////////////////////////////////////
	virtual void HandleEvent(CEvent* pEvent) = 0;
};